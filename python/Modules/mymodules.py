class calculateTwo:
    def add(a,b):
        return (a+b);
    def substract(c,d):
        return (c-d);
    def divide(e,f):
        return (e/f);
    def multiply(g,h):
        return (g*h);
    def modulo(i,j):
        return (i%j);
class calculateSquare:
    def area(k):
        return (k*k);
    def perimeter(s):
        return (4*s);
    def volume(l):
        return (l*l*l);
    
    
